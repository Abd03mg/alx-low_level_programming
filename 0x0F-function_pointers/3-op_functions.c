/**
 * op_add - function that return sum of two numbers.
 * @a: first arg.
 * @b: second arg.
 * Return: sum of two nums.
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function that return sub of two numbers.
 * @a: first arg.
 * @b: second arg.
 * Return: sub of two nums.
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function that return mul of two numbers.
 * @a: first arg.
 * @b: second arg.
 * Return: mul of two nums.
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function that return div of two numbers.
 * @a: first arg.
 * @b: second arg.
 * Return: div of two nums.
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function that return mod of two numbers.
 * @a: first arg.
 * @b: second arg.
 * Return: mod of two nums.
 */

int op_mod(int a, int b)
{
	return (a % b);
}

